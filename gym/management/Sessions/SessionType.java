package gym.management.Sessions;

/**
 * The {@code SessionType} enum represents the different types of gym sessions offered in the system.
 * Each constant defines a specific type of session that can be created and managed, ensuring type safety
 * and consistency across the system when dealing with session classifications.
 */
public enum SessionType {

    /**
     * Represents a Pilates session.
     */
    Pilates,

    /**
     * Represents a Machine Pilates session.
     */
    MachinePilates,

    /**
     * Represents a Thai Boxing session.
     */
    ThaiBoxing,

    /**
     * Represents a Ninja session.
     */
    Ninja;

}